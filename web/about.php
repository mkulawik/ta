<h2>Birth of Fadviews</h2>
<br>
<p>
When choosing a movie to watch at home with the family, Arthur would check online to see movie ratings to make a decision. At times, he found himself faced with the problem of selecting from several movies that scored about the same. This gave him the idea of sub categories which offers a better reason on how a movie gets its final score <a href="rating_system.php#1"> (see 8mm of film)</a>.  He also thought that a majority of actors were under appreciated and that people, like himself, would watch a movie just for the actor's performance. He believes that a badly rated movie will mask an actor's wonderful performance since it will garner less of an audience.  In that sense, he thought that actors too should be rated but through a system that would make acting more visible and balanced <a href="rating_system.php#2"> (see Take 5 of film).</a> Further development of the idea later revealed that fans too should be recognized for their support of movies and actors.  This 'fan appreciation' can be manifested in many forms such as posting monologues, musical arrangements, reenactments, prop memorabilia's, parodies, movie reviews and more, all parked in the most suitable of locations <a href="gallery.php#2"> (see Gallery)</a>.<br> If you think about it, what are shows and actors without the fans to acknowledge them?
<br>
<br>
Thus, Fadviews was born.
